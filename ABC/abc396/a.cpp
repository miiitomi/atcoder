void solve() {
    int N;
    cin >> N;
    vector<int> A(N);
    for (int i = 0; i < N; i++) {
        cin >> A[i];
    }

    for (int i = 1; i < N-1; i++) {
        if (A[i-1] == A[i] && A[i] == A[i+1]) {
            cout << "Yes\n";
            return;
        }
    }
    cout << "No\n";
}

