void solve() {
    ll N;
    cin >> N;
    vector<ll> A(N);
    for (int i = 0; i < N; i++) cin >> A[i];

    for (int i = 0; i < N-1; i++) {
        if (A[0]*A[i+1] != A[1]*A[i]) {
            cout << "No\n";
            return;
        }
    }
    cout << "Yes\n";
}

