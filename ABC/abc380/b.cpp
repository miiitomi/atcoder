void solve() {
    string S;
    cin >> S;
    vector<int> A;
    for (char c : S) {
        if (c == '|') A.push_back(0);
        else A.back()++;
    }
    A.pop_back();
    for (int a : A) cout << a << " ";
    cout << endl;
}

